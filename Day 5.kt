                           // using for array
 val fruits = arrayOf("Apple", "Banana", "Cherry")

 // Access and print the first fruit
 println("First fruit: ${fruits[0]}")

 // Modify an element
 fruits[1] = "Blueberry"

 // Print all fruits
 for (fruit in fruits) {
     println(fruit)
 }

 // Print the size of the array
 println("Number of fruits: ${fruits.size}")
